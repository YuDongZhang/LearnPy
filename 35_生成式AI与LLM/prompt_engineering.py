print('''
from langchain.prompts import PromptTemplate
from langchain.chains import LLMChain

# 定义Prompt模板
template = """你是一个{role}。
请用{style}风格回答以下问题:
问题: {question}

要求:
1. 长度: {length}
2. 格式: {format}
"""

prompt = PromptTemplate(
    template=template,
    input_variables=["role", "style", "question", "length", "format"]
)

# 创建Chain
chain = LLMChain(llm=llm, prompt=prompt)

# 使用
result = chain.run(
    role="Python专家",
    style="技术博客",
    question="什么是装饰器?",
    length="500字",
    format="Markdown"
)
''')

