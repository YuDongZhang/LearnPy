print("""
1.1 什么是Skills？
============================================================

Skills是一种让AI助手扩展能力的机制。它允许开发者定义
特定的功能模块，使AI能够执行特定任务或提供专业领域知识。

简单来说，Skill就像是一个"插件"，为AI添加新技能。

1.2 Skill的核心特性
============================================================

【可扩展性】
  - 动态添加新能力
  - 无需修改核心AI系统
  - 按需加载和卸载

【模块化】
  - 每个Skill专注单一功能
  - 清晰的职责边界
  - 便于测试和维护

【可复用】
  - 一次定义，多次使用
  - 跨项目共享
  - 社区贡献

【可组合】
  - 多个Skill协同工作
  - 灵活的任务编排
  - 解决复杂问题


1.3 Skill vs 传统AI功能
============================================================

| 特性 | 传统AI | Skills |
|------|--------|--------|
| 扩展方式 | 重新训练 | 即插即用 |
| 更新频率 | 慢 | 快 |
| 定制化 | 有限 | 高度定制 |
| 维护成本 | 高 | 低 |
| 社区参与 | 困难 | 容易 |
""")


