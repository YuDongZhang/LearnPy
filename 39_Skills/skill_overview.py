print("""
============================================================
6. Skill工作流程
============================================================

6.1 基本流程
============================================================

步骤1：用户请求
  用户用自然语言提出请求
  
  示例：
  "帮我审查这段代码"

步骤2：意图识别
  AI分析用户意图，选择合适的Skill
  
  识别结果：
  - Skill: code-reviewer
  - 参数: {code: "..."}

步骤3：Skill执行
  执行选中的Skill
  
  执行过程：
  - 加载Skill
  - 验证参数
  - 执行逻辑
  - 返回结果

步骤4：结果处理
  格式化并返回给用户
  
  输出：
  - 审查意见
  - 改进建议
  - 代码评分


6.2 复杂流程
============================================================

多Skill协作示例：

用户请求：
"分析这个项目的代码质量，并生成报告"

处理流程：

1. 代码扫描Skill
   → 收集代码指标

2. 代码审查Skill
   → 分析代码问题

3. 测试覆盖Skill
   → 检查测试情况

4. 文档生成Skill
   → 生成综合报告

5. 整合输出
   → 最终报告


6.3 错误处理
============================================================

【参数错误】
  - 提示用户补充参数
  - 提供参数示例
  - 建议默认值

【执行错误】
  - 捕获异常
  - 提供错误信息
  - 建议解决方案

【超时处理】
  - 设置超时限制
  - 提供重试选项
  - 返回部分结果
""")


