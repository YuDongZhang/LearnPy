print("""
============================================================
2. Skill设计思想
============================================================

2.1 核心原则
============================================================

【单一职责】
  每个Skill只专注完成一件事
  
  示例：
  - code-reviewer: 代码审查
  - document-generator: 文档生成
  - data-analyzer: 数据分析

【清晰接口】
  Skill有明确定义的输入输出
  
  输入参数：
  - 必需参数（required）
  - 可选参数（optional）
  
  输出结果：
  - 标准化格式
  - 错误处理

【独立运行】
  Skill应该能够独立运行
  
  自包含：
  - 不依赖其他Skill
  - 独立的配置
  - 独立的资源

2.2 设计模式
============================================================

【命令模式】
  用户触发 → Skill执行 → 返回结果
  
  适用场景：
  - 一次性任务
  - 明确的目标
  - 简单流程

【代理模式】
  Skill作为AI的代理
  
  适用场景：
  - 外部API调用
  - 系统操作
  - 权限控制

【组合模式】
  多个Skill协作
  
  适用场景：
  - 复杂任务
  - 工作流
  - 多步骤流程


2.3 架构分层
============================================================

【用户层】
  用户请求 → 自然语言
  
【意图识别层】
  解析用户意图 → 选择Skill
  
【Skill执行层】
  执行Skill逻辑 → 返回结果
  
【结果处理层】
  格式化结果 → 反馈用户
""")


